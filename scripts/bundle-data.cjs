// Bundle all section JSON + archive data into a single data.js file
// This allows the static site to load all data without individual fetches
// (CommonJS format so it can be required/loaded easily)

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SITE_DATA = path.join(ROOT, 'site', 'data');
const ARCHIVE_DIR = path.join(SITE_DATA, 'archive');
const OUTPUT = path.join(SITE_DATA, 'data.js');

function readJSONSafe(filepath) {
  try {
    if (!fs.existsSync(filepath)) return null;
    return JSON.parse(fs.readFileSync(filepath, 'utf-8'));
  } catch {
    return null;
  }
}

function main() {
  console.log('Bundling data into data.js...\n');

  const sections = [
    'core-businesses',
    'ip-legal',
    'growth-areas',
    'macro-environment',
    'competitors',
    'stakeholders',
  ];

  const bundle = {
    generatedAt: new Date().toISOString(),
    sections: {},
    archive: {},
    sources: [],
    latest: null,
  };

  // Load section data
  for (const sectionId of sections) {
    const data = readJSONSafe(path.join(SITE_DATA, `${sectionId}.json`));
    if (data) {
      bundle.sections[sectionId] = data;
      console.log(`  Section ${sectionId}: ${data.articles?.length || 0} articles`);
      // Use generatedAt from section data
      if (data.generatedAt) bundle.generatedAt = data.generatedAt;
    } else {
      bundle.sections[sectionId] = { date: '', generatedAt: '', section: sectionId, count: 0, articles: [] };
      console.log(`  Section ${sectionId}: NOT FOUND, creating empty`);
    }
  }

  // Load archive data (last 14 days)
  if (fs.existsSync(ARCHIVE_DIR)) {
    const files = fs.readdirSync(ARCHIVE_DIR).filter(f => f.endsWith('.json')).sort().reverse();
    const recentFiles = files.slice(0, 14);
    for (const file of recentFiles) {
      const dateKey = file.replace('.json', '');
      const data = readJSONSafe(path.join(ARCHIVE_DIR, file));
      if (data) {
        bundle.archive[dateKey] = data;
        console.log(`  Archive ${dateKey}: ${data.articles?.length || 0} articles`);
      }
    }
  }

  // Load sources
  const sources = readJSONSafe(path.join(SITE_DATA, 'sources.json'));
  if (sources) {
    bundle.sources = sources;
    console.log(`  Sources: ${sources.length} entries`);
  }

  // Load latest metadata
  const latest = readJSONSafe(path.join(SITE_DATA, 'latest.json'));
  if (latest) {
    bundle.latest = latest;
    console.log(`  Latest: ${latest.date} (${latest.totalArticles} total articles)`);
  }

  // Write data.js
  const jsContent = `// Auto-generated by bundle-data.cjs — do not edit manually
// Generated at: ${new Date().toISOString()}
window.__NEWS_DATA__ = ${JSON.stringify(bundle, null, 2)};
`;

  fs.writeFileSync(OUTPUT, jsContent, 'utf-8');
  console.log(`\nBundled data written to ${path.relative(ROOT, OUTPUT)}`);
  console.log(`File size: ${(fs.statSync(OUTPUT).size / 1024).toFixed(1)} KB\n`);
}

main();
